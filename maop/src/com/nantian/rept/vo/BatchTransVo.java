package com.nantian.rept.vo;

import java.io.Serializable;

public class BatchTransVo implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	private String aplCode;
	
	private String batchDate;
	
	private String batchName;
	
	private String batchStartTime;
	
	private String batchEndTime;
	
	private String batchExeTime;
	
	private String endFlag;
	
	private String comments;

	public String getAplCode() {
		return aplCode;
	}

	public void setAplCode(String aplCode) {
		this.aplCode = aplCode;
	}

	public String getBatchDate() {
		return batchDate;
	}

	public void setBatchDate(String batchDate) {
		this.batchDate = batchDate;
	}

	public String getBatchName() {
		return batchName;
	}

	public void setBatchName(String batchName) {
		this.batchName = batchName;
	}

	public String getBatchStartTime() {
		return batchStartTime;
	}

	public void setBatchStartTime(String batchStartTime) {
		this.batchStartTime = batchStartTime;
	}

	public String getBatchEndTime() {
		return batchEndTime;
	}

	public void setBatchEndTime(String batchEndTime) {
		this.batchEndTime = batchEndTime;
	}

	public String getBatchExeTime() {
		return batchExeTime;
	}

	public void setBatchExeTime(String batchExeTime) {
		this.batchExeTime = batchExeTime;
	}

	public String getEndFlag() {
		return endFlag;
	}

	public void setEndFlag(String endFlag) {
		this.endFlag = endFlag;
	}

	public String getComments() {
		return comments;
	}

	public void setComments(String comments) {
		this.comments = comments;
	}
	
}
