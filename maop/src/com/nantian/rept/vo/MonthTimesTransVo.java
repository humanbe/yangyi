package com.nantian.rept.vo;

import java.io.Serializable;

public class MonthTimesTransVo implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	private String aplCode;
	
	private String countMonth;
	
	private String peakItem;
	
	private String peakDate;
	
	private String peakValue;

	public String getAplCode() {
		return aplCode;
	}

	public void setAplCode(String aplCode) {
		this.aplCode = aplCode;
	}

	public String getCountMonth() {
		return countMonth;
	}

	public void setCountMonth(String countMonth) {
		this.countMonth = countMonth;
	}

	public String getPeakItem() {
		return peakItem;
	}

	public void setPeakItem(String peakItem) {
		this.peakItem = peakItem;
	}

	public String getPeakDate() {
		return peakDate;
	}

	public void setPeakDate(String peakDate) {
		this.peakDate = peakDate;
	}

	public String getPeakValue() {
		return peakValue;
	}

	public void setPeakValue(String peakValue) {
		this.peakValue = peakValue;
	}
}
