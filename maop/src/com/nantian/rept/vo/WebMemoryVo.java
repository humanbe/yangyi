package com.nantian.rept.vo;

import java.io.Serializable;

public class WebMemoryVo implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	private String aplCode;
	
	private String srvCode;
	
	private String weblogicServer;
	
	private String monitorDate;
	
	private String monitorTime;
	
	private String totalMemory;
	
	private String idleMemory;
	
	private String usedMemory;
	
	private String memoryRate;
	
	private String weblogicPort;

	public String getAplCode() {
		return aplCode;
	}

	public void setAplCode(String aplCode) {
		this.aplCode = aplCode;
	}

	public String getSrvCode() {
		return srvCode;
	}

	public void setSrvCode(String srvCode) {
		this.srvCode = srvCode;
	}

	public String getWeblogicServer() {
		return weblogicServer;
	}

	public void setWeblogicServer(String weblogicServer) {
		this.weblogicServer = weblogicServer;
	}

	public String getMonitorDate() {
		return monitorDate;
	}

	public void setMonitorDate(String monitorDate) {
		this.monitorDate = monitorDate;
	}

	public String getMonitorTime() {
		return monitorTime;
	}

	public void setMonitorTime(String monitorTime) {
		this.monitorTime = monitorTime;
	}

	public String getTotalMemory() {
		return totalMemory;
	}

	public void setTotalMemory(String totalMemory) {
		this.totalMemory = totalMemory;
	}

	public String getIdleMemory() {
		return idleMemory;
	}

	public void setIdleMemory(String idleMemory) {
		this.idleMemory = idleMemory;
	}

	public String getUsedMemory() {
		return usedMemory;
	}

	public void setUsedMemory(String usedMemory) {
		this.usedMemory = usedMemory;
	}

	public String getMemoryRate() {
		return memoryRate;
	}

	public void setMemoryRate(String memoryRate) {
		this.memoryRate = memoryRate;
	}

	public String getWeblogicPort() {
		return weblogicPort;
	}

	public void setWeblogicPort(String weblogicPort) {
		this.weblogicPort = weblogicPort;
	}

}
