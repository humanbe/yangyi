package com.nantian.rept.vo;

import java.io.Serializable;

public class WeekTransVo implements Serializable{
	
	private static final long serialVersionUID = 1L;

	private String aplCode;
	
	private String countWeek;
	
	private String weekTotalTrans;
	
	private String weekPeakTrans;

	public String getAplCode() {
		return aplCode;
	}

	public void setAplCode(String aplCode) {
		this.aplCode = aplCode;
	}

	public String getCountWeek() {
		return countWeek;
	}

	public void setCountWeek(String countWeek) {
		this.countWeek = countWeek;
	}

	public String getWeekTotalTrans() {
		return weekTotalTrans;
	}

	public void setWeekTotalTrans(String weekTotalTrans) {
		this.weekTotalTrans = weekTotalTrans;
	}

	public String getWeekPeakTrans() {
		return weekPeakTrans;
	}

	public void setWeekPeakTrans(String weekPeakTrans) {
		this.weekPeakTrans = weekPeakTrans;
	}

}
