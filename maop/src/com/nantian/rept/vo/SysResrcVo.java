package com.nantian.rept.vo;

import java.io.Serializable;

public class SysResrcVo implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	private String aplCode;
	
	private String transDate;
	
	private String srvCode;
	
	private String minPoint;
	
	private String cpuPercent;
	
	private String memPercent;
	
	private String diskPercent;

	public String getAplCode() {
		return aplCode;
	}

	public void setAplCode(String aplCode) {
		this.aplCode = aplCode;
	}

	public String getTransDate() {
		return transDate;
	}

	public void setTransDate(String transDate) {
		this.transDate = transDate;
	}

	public String getSrvCode() {
		return srvCode;
	}

	public void setSrvCode(String srvCode) {
		this.srvCode = srvCode;
	}

	public String getMinPoint() {
		return minPoint;
	}

	public void setMinPoint(String minPoint) {
		this.minPoint = minPoint;
	}

	public String getCpuPercent() {
		return cpuPercent;
	}

	public void setCpuPercent(String cpuPercent) {
		this.cpuPercent = cpuPercent;
	}

	public String getMemPercent() {
		return memPercent;
	}

	public void setMemPercent(String memPercent) {
		this.memPercent = memPercent;
	}

	public String getDiskPercent() {
		return diskPercent;
	}

	public void setDiskPercent(String diskPercent) {
		this.diskPercent = diskPercent;
	}
	
	
}
