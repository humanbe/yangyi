package com.nantian.smrpt.vo;

import java.io.Serializable;
import java.util.Date;

public class DayDiskVo implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	/**主机名*/
	private String hostName;
	
	/**日期*/
	private Date date;
	
	/**日峰值*/
	private Float dayPeak;
	
	/**日均值*/
	private Float dayAvg;
	
	/**联机峰值*/
	private Float onlinePeak;
	
	/**联机均值*/
	private Float onlineAvg;
	
	/**批量峰值*/
	private Float batchPeak;
	
	/**批量均值*/
	private Float batchAvg;

	public String getHostName() {
		return hostName;
	}

	public void setHostName(String hostName) {
		this.hostName = hostName;
	}

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}

	public Float getDayPeak() {
		return dayPeak;
	}

	public void setDayPeak(Float dayPeak) {
		this.dayPeak = dayPeak;
	}

	public Float getDayAvg() {
		return dayAvg;
	}

	public void setDayAvg(Float dayAvg) {
		this.dayAvg = dayAvg;
	}

	public Float getOnlinePeak() {
		return onlinePeak;
	}

	public void setOnlinePeak(Float onlinePeak) {
		this.onlinePeak = onlinePeak;
	}

	public Float getOnlineAvg() {
		return onlineAvg;
	}

	public void setOnlineAvg(Float onlineAvg) {
		this.onlineAvg = onlineAvg;
	}

	public Float getBatchPeak() {
		return batchPeak;
	}

	public void setBatchPeak(Float batchPeak) {
		this.batchPeak = batchPeak;
	}

	public Float getBatchAvg() {
		return batchAvg;
	}

	public void setBatchAvg(Float batchAvg) {
		this.batchAvg = batchAvg;
	}

}
