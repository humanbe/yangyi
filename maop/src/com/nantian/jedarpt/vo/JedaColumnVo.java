package com.nantian.jedarpt.vo;

import java.io.Serializable;

/**
* 报表定制  - 字段表
* @author linaWang
* 
*/
public class JedaColumnVo implements Serializable{

		private static final long serialVersionUID = 1L;

		/**字段编号*/
		private int column_code ; 
		
		/**字段名称*/
		private String column_en_name;  
		
		/**字段别名*/
		private String column_ch_name;  
		
		/**描述*/
		private String column_desc;  
		
		/**删除标示*/
		private String delete_flag;  
		
		/**是否关联转换*/
		private String is_trans;  
		
		/**数据字典中数据项编码*/
		private String dic_code;

		public int getColumn_code() {
			return column_code;
		}

		public void setColumn_code(int column_code) {
			this.column_code = column_code;
		}

		public String getColumn_en_name() {
			return column_en_name;
		}

		public void setColumn_en_name(String column_en_name) {
			this.column_en_name = column_en_name;
		}

		public String getColumn_ch_name() {
			return column_ch_name;
		}

		public void setColumn_ch_name(String column_ch_name) {
			this.column_ch_name = column_ch_name;
		}

		public String getColumn_desc() {
			return column_desc;
		}

		public void setColumn_desc(String column_desc) {
			this.column_desc = column_desc;
		}

		public String getDelete_flag() {
			return delete_flag;
		}

		public void setDelete_flag(String delete_flag) {
			this.delete_flag = delete_flag;
		}

		public String getIs_trans() {
			return is_trans;
		}

		public void setIs_trans(String is_trans) {
			this.is_trans = is_trans;
		}

		public String getDic_code() {
			return dic_code;
		}

		public void setDic_code(String dic_code) {
			this.dic_code = dic_code;
		}
		
}
