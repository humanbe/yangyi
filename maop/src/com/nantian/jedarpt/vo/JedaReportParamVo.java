package com.nantian.jedarpt.vo;

import java.io.Serializable;

/**
* 报表定制  - 报表参数表
* @author linaWang
* 
*/
public class JedaReportParamVo implements Serializable{

		private static final long serialVersionUID = 1L;

		/**参数编号*/
		private int param_code ; 
		
		/**报表编号*/
		private int report_code;  
		
		/**参数名称*/
		private String param_en_name;  
		
		/**别名*/
		private String param_ch_name;  
		
		/**参数类型*/
		private String param_type;  
		
		/**默认值*/
		private String default_value;  
		
		/**数据字典中数据项编码*/
		private String dic_code;
		
		/**删除标示*/
		private String delete_flag;

		public int getParam_code() {
			return param_code;
		}

		public void setParam_code(int param_code) {
			this.param_code = param_code;
		}

		public int getReport_code() {
			return report_code;
		}

		public void setReport_code(int report_code) {
			this.report_code = report_code;
		}

		public String getParam_en_name() {
			return param_en_name;
		}

		public void setParam_en_name(String param_en_name) {
			this.param_en_name = param_en_name;
		}

		public String getParam_ch_name() {
			return param_ch_name;
		}

		public void setParam_ch_name(String param_ch_name) {
			this.param_ch_name = param_ch_name;
		}

		public String getParam_type() {
			return param_type;
		}

		public void setParam_type(String param_type) {
			this.param_type = param_type;
		}

		public String getDefault_value() {
			return default_value;
		}

		public void setDefault_value(String default_value) {
			this.default_value = default_value;
		}

		public String getDic_code() {
			return dic_code;
		}

		public void setDic_code(String dic_code) {
			this.dic_code = dic_code;
		}

		public String getDelete_flag() {
			return delete_flag;
		}

		public void setDelete_flag(String delete_flag) {
			this.delete_flag = delete_flag;
		}
}
