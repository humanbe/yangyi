package com.nantian.jedarpt.vo;

import java.io.Serializable;

/**
* 报表定制 - 报表规则关联表
* @author linaWang
* 
*/
public class JedaReportRuleVo implements Serializable{

		private static final long serialVersionUID = 1L;

		/**报表编号*/
		private int report_code ; 
		
		/**规则编号*/
		private int rule_code;

		public int getReport_code() {
			return report_code;
		}

		public void setReport_code(int report_code) {
			this.report_code = report_code;
		}

		public int getRule_code() {
			return rule_code;
		}

		public void setRule_code(int rule_code) {
			this.rule_code = rule_code;
		}

}
