package com.nantian.demo.vo;

import java.io.Serializable;

/**
 * @author <a href="mailto:name@nantian.com.cn">name</a>
 * 
 */
public class CallInfoVo implements Serializable{
	
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String callId;
	private String calledId;
	private char headNode;
	private char calledFlag;
	private char commprotocol;
	private char callway;
	private String condition;
	private Integer callnum;
	public String getCallId() {
		return callId;
	}
	public void setCallId(String callId) {
		this.callId = callId;
	}
	public String getCalledId() {
		return calledId;
	}
	public void setCalledId(String calledId) {
		this.calledId = calledId;
	}
	public char getHeadNode() {
		return headNode;
	}
	public void setHeadNode(char headNode) {
		this.headNode = headNode;
	}
	public char getCalledFlag() {
		return calledFlag;
	}
	public void setCalledFlag(char calledFlag) {
		this.calledFlag = calledFlag;
	}
	public char getCommprotocol() {
		return commprotocol;
	}
	public void setCommprotocol(char commprotocol) {
		this.commprotocol = commprotocol;
	}
	public char getCallway() {
		return callway;
	}
	public void setCallway(char callway) {
		this.callway = callway;
	}
	public String getCondition() {
		return condition;
	}
	public void setCondition(String condition) {
		this.condition = condition;
	}
	public Integer getCallnum() {
		return callnum;
	}
	public void setCallnum(Integer callnum) {
		this.callnum = callnum;
	}
} 