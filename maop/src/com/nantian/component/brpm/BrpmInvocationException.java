package com.nantian.component.brpm;

/**
 * 调用BRPM异常
 * @author user
 *
 */
public class BrpmInvocationException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	public BrpmInvocationException(){
		super();
	}
	
	/**
	 * @param message
	 */
	public BrpmInvocationException(String message){
		super(message);
	}
	
	/**
	 * @param cause
	 */
	public BrpmInvocationException(Throwable cause) {
		super(cause);
	}
	
	/**
	 * @param message
	 * @param cause
	 */
	public BrpmInvocationException(String message, Throwable cause) {
		super(message, cause);
	}

}
