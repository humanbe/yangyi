package com.nantian.toolbox.vo;

import java.io.Serializable;

/**
 * @author ky
 * 
 */
public class ToolBoxScriptInfoVo implements Serializable {
	private static final long serialVersionUID = 1L;
	/**工具编号*/
	private String tool_code;
	/**脚本名称*/
	private String shell_name;
	/**服务器分组*/
	private String server_group;
	/**组/服务器*/
	private String group_server_flag;
	/**操作用户指定*/
	private String os_user_flag;
	/**操作用户*/
	private String os_user;
	/**操作系统分类*/
	private String os_type;
	/**工具存放场所分类*/
	private String position_type;
	/**工具字符集*/
	private String tool_charset;

	
	public String getTool_code() {
		return tool_code;
	}

	public void setTool_code(String tool_code) {
		this.tool_code = tool_code;
	}

	public String getShell_name() {
		return shell_name;
	}

	public void setShell_name(String shell_name) {
		this.shell_name = shell_name;
	}

	public String getServer_group() {
		return server_group;
	}

	public void setServer_group(String server_group) {
		this.server_group = server_group;
	}

	public String getGroup_server_flag() {
		return group_server_flag;
	}

	public void setGroup_server_flag(String group_server_flag) {
		this.group_server_flag = group_server_flag;
	}

	public String getOs_user_flag() {
		return os_user_flag;
	}

	public void setOs_user_flag(String os_user_flag) {
		this.os_user_flag = os_user_flag;
	}

	public String getOs_type() {
		return os_type;
	}

	public void setOs_type(String os_type) {
		this.os_type = os_type;
	}

	public String getPosition_type() {
		return position_type;
	}

	public void setPosition_type(String position_type) {
		this.position_type = position_type;
	}

	public String getTool_charset() {
		return tool_charset;
	}

	public void setTool_charset(String tool_charset) {
		this.tool_charset = tool_charset;
	}

	public String getOs_user() {
		return os_user;
	}

	public void setOs_user(String os_user) {
		this.os_user = os_user;
	}
	
	




}
